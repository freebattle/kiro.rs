#[derive(Deserialize)]
pub struct RequestLogQuery {
    pub page: Option<usize>,
    #[serde(rename = "pageSize")]
    pub page_size: Option<usize>,
}

/// GET /api/admin/requests
/// 获取当天请求记录（分页）
pub async fn get_request_logs(
    State(state): State<AdminState>,
    Query(query): Query<RequestLogQuery>,
) -> impl IntoResponse {
    let page = query.page.unwrap_or(1).max(1);
    let page_size = query.page_size.unwrap_or(50).min(200);
    let (records, total) = state.request_log.get_today_paged(page, page_size);
    Json(serde_json::json!({
        "records": records,
        "total": total,
        "page": page,
        "pageSize": page_size,
    }))
}

/// GET /api/admin/requests/stats
/// 获取当天请求统计
pub async fn get_request_stats(State(state): State<AdminState>) -> impl IntoResponse {
    let stats = state.request_log.get_today_stats();
    Json(stats)
}

#[derive(Deserialize)]
pub struct UsageStatsQuery {
    pub month: Option<String>,
}

/// GET /api/admin/usage-stats
/// 获取月度用量统计
pub async fn get_usage_stats(
    State(state): State<AdminState>,
    Query(query): Query<UsageStatsQuery>,
) -> impl IntoResponse {
    let month = query.month.unwrap_or_else(|| {
        let now = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap()
            .as_secs();
        let ts = now + 8 * 3600;
        let days = ts / 86400;
        let z = days + 719468;
        let era = z / 146097;
        let doe = z - era * 146097;
        let yoe = (doe - doe / 1460 + doe / 36524 - doe / 146096) / 365;
        let y = yoe + era * 400;
        let doy = doe - (365 * yoe + yoe / 4 - yoe / 100);
        let mp = (5 * doy + 2) / 153;
        let m = if mp < 10 { mp + 3 } else { mp - 9 };
        let y = if m <= 2 { y + 1 } else { y };
        format!("{:04}-{:02}", y, m)
    });
    let usage = state.usage_stats.get_month(&month);
    Json(usage)
}
